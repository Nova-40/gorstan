#!/usr/bin/env node
/*
  Simple generator for optimized image manifest.
  Scans public/images for image files and creates a manifest mapping logical paths
  (relative to /images/) to available variants (avif, webp, fallback).

  Also writes a small TypeScript module at src/lib/optimized.ts that exports the
  manifest and a helper pickOptimized(key).

  This is intentionally conservative and fast — it will not transcode images.
  Add this as a pre-build step so fresh clones produce the artifacts needed by
  the app and TypeScript compiler.
*/
const fs = require('fs');
const path = require('path');

const publicImagesDir = path.resolve(__dirname, '..', 'public', 'images');
const outJson = path.resolve(__dirname, '..', 'public', 'images', '_optimized.json');
const outTs = path.resolve(__dirname, '..', 'src', 'lib', 'optimized.ts');

function walk(dir) {
  const res = [];
  if (!fs.existsSync(dir)) return res;
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      res.push(...walk(full));
    } else {
      res.push(full);
    }
  }
  return res;
}

function buildManifest() {
  const files = walk(publicImagesDir);
  const map = Object.create(null);
  for (const f of files) {
    const rel = path.relative(publicImagesDir, f).replace(/\\\\/g, '/');
    const ext = path.extname(rel).toLowerCase();
    const base = rel.replace(/\.[^/.]+$/, '');
    // skip hidden files
    if (rel.startsWith('.')) continue;
    map[base] = map[base] || {};
    if (ext === '.avif') map[base].avif = '/images/' + rel;
    else if (ext === '.webp') map[base].webp = '/images/' + rel;
    else if (ext === '.jpg' || ext === '.jpeg' || ext === '.png' || ext === '.gif' || ext === '.svg') {
      // treat first encountered raster as fallback if none exists yet
      map[base].fallback = map[base].fallback || '/images/' + rel;
    } else {
      // ignore other extensions for now
    }
  }
  return map;
}

function ensureDir(filePath) {
  const d = path.dirname(filePath);
  if (!fs.existsSync(d)) fs.mkdirSync(d, { recursive: true });
}

function writeOutputs(manifest) {
  ensureDir(outJson);
  fs.writeFileSync(outJson, JSON.stringify(manifest, null, 2), 'utf8');
  ensureDir(outTs);
  const tsContent = `// Auto-generated by scripts/generate-optimized.cjs - do not edit\n` +
    `export type OptimizedEntry = { avif?: string; webp?: string; fallback?: string; lqip?: string };\n` +
    `const manifest: Record<string, OptimizedEntry> = ${JSON.stringify(manifest, null, 2)} as any;\n\n` +
    `export default manifest;\n\n` +
    `export function pickOptimized(key: string): OptimizedEntry | null {\n` +
    `  return (manifest as any)[key] || null;\n` +
    `}\n`;
  fs.writeFileSync(outTs, tsContent, 'utf8');
}

function main() {
  try {
    const manifest = buildManifest();
    writeOutputs(manifest);
    console.log('Wrote', outJson);
    console.log('Wrote', outTs);
  } catch (err) {
    console.error('generate-optimized failed:', err);
    process.exit(1);
  }
}

main();
